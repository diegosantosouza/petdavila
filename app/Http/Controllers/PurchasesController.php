    {
        $purchase = new Purchases();
        $purchase->fill($request->all());
        $purchase->date = new \DateTime();
        $purchase->save();
        return redirect()->route('finance.purchase')->with(['color' => 'green', 'message' => 'Cadastrado com sucesso!']);
    }

